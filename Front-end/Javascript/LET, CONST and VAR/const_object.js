const person = { // Declaring a constant object
    name: 'John', // Assigning the value of the string "John" to the property name of the object person
    age: 30 // Assigning the value of the number 30 to the property age of the object person
};

console.log(person); // { name: 'John', age: 30 }

person.name = 'Jane'; // Reassigning the value of a property

console.log(person); // { name: 'Jane', age: 30 }